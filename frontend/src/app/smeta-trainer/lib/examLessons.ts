const VISITED_KEY = "smeta-trainer:exam-lessons-visited:v1";

export function markLessonVisited(taskId: string): void {
  if (typeof window === "undefined") return;
  try {
    const raw = localStorage.getItem(VISITED_KEY);
    const set = new Set<string>(raw ? JSON.parse(raw) : []);
    set.add(taskId);
    localStorage.setItem(VISITED_KEY, JSON.stringify(Array.from(set)));
  } catch {
    /* ignore */
  }
}

export function getVisitedLessons(): Set<string> {
  if (typeof window === "undefined") return new Set();
  try {
    const raw = localStorage.getItem(VISITED_KEY);
    if (!raw) return new Set();
    return new Set<string>(JSON.parse(raw));
  } catch {
    return new Set();
  }
}

export function isLessonVisited(taskId: string): boolean {
  return getVisitedLessons().has(taskId);
}

export function findLesson(taskId: string): ExamLesson | undefined {
  return EXAM_LESSONS[taskId];
}

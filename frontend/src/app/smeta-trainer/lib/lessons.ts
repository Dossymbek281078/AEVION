// ── Прогресс по урокам (localStorage) ────────────────────────────────

const PROGRESS_KEY = "aevion-smeta-lessons-progress-v1";

export interface LessonProgress {
  completed: boolean;
  quizScore?: number; // 0..100
  ts: number;
}

export function loadLessonProgress(): Record<string, LessonProgress> {
  if (typeof window === "undefined") return {};
  try {
    const raw = localStorage.getItem(PROGRESS_KEY);
    return raw ? (JSON.parse(raw) as Record<string, LessonProgress>) : {};
  } catch {
    return {};
  }
}

export function saveLessonProgress(id: string, p: LessonProgress): void {
  if (typeof window === "undefined") return;
  try {
    const all = loadLessonProgress();
    all[id] = { ...p, ts: Date.now() };
    localStorage.setItem(PROGRESS_KEY, JSON.stringify(all));
  } catch {}
}

/** Доля пройденных уроков уровня (0..1). */
export function levelLessonsCompletion(level: number): number {
  const lessons = getLessonsForLevel(level);
  if (lessons.length === 0) return 0;
  const all = loadLessonProgress();
  const done = lessons.filter((l) => all[l.id]?.completed).length;
  return done / lessons.length;
}

// ── Проверка ответа на тест ──────────────────────────────────────────

export function checkQuizAnswer(quiz: Quiz, answer: string | number): boolean {
  if (quiz.kind === "mc") {
    return Number(answer) === quiz.correct;
  }
  // fill: case-insensitive trim, числа сравнивать с допуском
  const a = String(answer).trim().toLowerCase();
  if (!a) return false;
  for (const acc of quiz.accepts) {
    const accNorm = acc.trim().toLowerCase();
    if (a === accNorm) return true;
    // числовое сравнение с допуском 1%
    const an = parseFloat(a.replace(",", ".").replace(/[^\d.\-]/g, ""));
    const accN = parseFloat(accNorm.replace(",", ".").replace(/[^\d.\-]/g, ""));
    if (!Number.isNaN(an) && !Number.isNaN(accN)) {
      const tol = Math.max(Math.abs(accN) * 0.01, 0.001);
      if (Math.abs(an - accN) <= tol) return true;
    }
  }
  return false;
}

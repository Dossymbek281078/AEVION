function matchKb(question: string): KbEntry | null {
  const q = question.toLowerCase();
  let best: KbEntry | null = null;
  let bestScore = 0;
  for (const entry of KB) {
    const score = entry.keywords.filter((kw) => q.includes(kw)).length;
    if (score > bestScore) { bestScore = score; best = entry; }
  }
  return bestScore > 0 ? best : null;
}

function contextualAdvice(lsr: Lsr, calc: LsrCalc): string {
  const tips: string[] = [];
  const totalPositions = lsr.sections.reduce((s, sec) => s + sec.positions.length, 0);

  if (totalPositions === 0) {
    return "Смета пуста. Начни с первого раздела: найди расценку на демонтаж в поиске слева (например, «ДЕМ-15») и нажми на неё — позиция добавится.";
  }

  // Проверка на отсутствие К=1.15
  const sectionsWithoutK = lsr.sections.filter((s) =>
    s.positions.length > 0 &&
    (s.category === "демонтажные" || s.category === "отделочные") &&
    s.positions.some((p) => p.coefficients.length === 0)
  );
  if (sectionsWithoutK.length > 0) {
    tips.push(`В разделах "${sectionsWithoutK.map((s) => s.title).join(", ")}" есть позиции без К=1.15. Школа действующая — коэффициент обязателен. Нажми «+К» на каждой строке и выбери «Действующий объект».`);
  }

  // Пустые разделы
  const emptySections = lsr.sections.filter((s) => s.positions.length === 0);
  if (emptySections.length > 0 && totalPositions > 2) {
    tips.push(`Пустые разделы: ${emptySections.map((s) => s.title).join(", ")}. Добавь в них позиции через поиск расценок.`);
  }

  // Шпатлёвка без грунтовки/окраски
  const hasShpat = lsr.sections.some((s) => s.positions.some((p) => p.rateCode.includes("15-02")));
  const hasPaint = lsr.sections.some((s) => s.positions.some((p) => p.rateCode.includes("15-04")));
  if (hasShpat && !hasPaint) {
    tips.push("Есть шпатлёвка стен, но нет окраски — обычно они идут вместе. Добавь расценку ОТД-15-04-001 в раздел окраски.");
  }

  // Есть демонтаж, но нет новых покрытий
  const hasDemont = lsr.sections.some((s) => s.category === "демонтажные" && s.positions.length > 0);
  const hasNewFloor = lsr.sections.some((s) => s.positions.some((p) => p.rateCode.includes("11-04") || p.rateCode.includes("11-03")));
  if (hasDemont && !hasNewFloor && totalPositions > 3) {
    tips.push("Есть демонтаж полов, но нет нового покрытия (ламинат/плитка). Не забудь добавить в раздел «Полы».");
  }

  if (tips.length === 0) {
    const direct = calc.sections.reduce((s, sc) => s + sc.direct, 0);
    return `Смета выглядит хорошо! ${totalPositions} позиций, прямые затраты ${(direct / 1000000).toFixed(2)} млн ₸. Проверь вкладку «ВОР» — заполнены ли формулы объёмов?`;
  }

  return tips[0];
}

export function askConsultant(question: string, lsr: Lsr, calc: LsrCalc): AiMessage {
  const q = question.toLowerCase().trim();

  // Контекстный вопрос
  if (q.includes("что мне") || q.includes("что не хватает") || q.includes("что добавить") || q.includes("следующий шаг") || q.includes("что ещё")) {
    return {
      role: "assistant",
      text: contextualAdvice(lsr, calc),
      ts: Date.now(),
    };
  }

  // Проверь мой объём / посмотри смету
  if (q.includes("проверь") || q.includes("посмотри") || q.includes("всё верно") || q.includes("правильно")) {
    return {
      role: "assistant",
      text: contextualAdvice(lsr, calc),
      ts: Date.now(),
    };
  }

  // База знаний
  const entry = matchKb(question);
  if (entry && entry.answer && entry.answer !== "__contextual__") {
    return {
      role: "assistant",
      text: entry.answer,
      refs: entry.refs,
      tip: entry.tip,
      ts: Date.now(),
    };
  }

  // Fallback
  return {
    role: "assistant",
    text: "Хороший вопрос! Попробуй спросить конкретнее: «Как считается НР?», «Зачем К=1.15?», «Как посчитать объём стен?» или «Что мне ещё нужно добавить?». Я знаю правила сметного дела РК и вижу твою текущую смету.",
    ts: Date.now(),
  };
}


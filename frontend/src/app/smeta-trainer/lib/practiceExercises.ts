export interface PracticeOption {
  text: string;
  /** Короткое объяснение, почему вариант неверный (для неправильных). */
  explainIfPicked?: string;
}

export interface PracticeExercise {
  id: string;
  /** AI-сценарий, который тренируем (для статистики и связи с lib/ai). */
  scenario:
    | "openings"
    | "doubleCount"
    | "missingCoef"
    | "winterSurcharge"
    | "indexMismatch"
    | "overheadMismatch"
    | "heightCoef";
  /** Заголовок задачи. */
  title: string;
  /** Кратчайший контекст: о каком объекте/работе речь. */
  context: string;
  /** Текст-«снимок» сметы или ситуации (моноширинный, до 6 строк). */
  snapshot: string;
  /** Подсказка, если студент промахнётся 2 раза (опционально). */
  hint: string;
  /** Вопрос. */
  question: string;
  options: PracticeOption[];
  /** Индекс правильного ответа в options. */
  correct: number;
  /** Подробное разъяснение после ответа. */
  explanation: string;
  /** На какой урок ссылаться при повторении. */
  lessonRef?: string;
  /** Норматив для ссылки. */
  normRef?: string;
}


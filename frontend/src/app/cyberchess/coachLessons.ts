/* ─── Storage helpers ─────────────────────────────────────── */
const LESSONS_KEY = "aevion_coach_lessons_v1";

export type LessonProgress = {
  id: string;
  startedAt: number;
  completedAt?: number;
  stepsCompleted: number;
};

export type LessonsState = {
  v: 1;
  byId: Record<string, LessonProgress>;
};

export function loadLessons(): LessonsState {
  if (typeof window === "undefined") return { v: 1, byId: {} };
  try {
    const raw = localStorage.getItem(LESSONS_KEY);
    if (!raw) return { v: 1, byId: {} };
    const r = JSON.parse(raw);
    if (r && r.v === 1 && typeof r.byId === "object") return r as LessonsState;
  } catch {}
  return { v: 1, byId: {} };
}

export function saveLessons(s: LessonsState) {
  try { localStorage.setItem(LESSONS_KEY, JSON.stringify(s)); } catch {}
}

export function isLessonComplete(state: LessonsState, id: string): boolean {
  return !!state.byId[id]?.completedAt;
}

export function lessonProgress(state: LessonsState, id: string): number {
  const p = state.byId[id];
  if (!p) return 0;
  if (p.completedAt) return 100;
  const lesson = LESSONS.find(l => l.id === id);
  if (!lesson) return 0;
  return Math.round((p.stepsCompleted / lesson.steps.length) * 100);
}

export function totalCompleted(state: LessonsState): number {
  return Object.values(state.byId).filter(p => p.completedAt).length;
}

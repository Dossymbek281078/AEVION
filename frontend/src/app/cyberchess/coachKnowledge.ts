/* ─── Экспорт ─── */
export const COACH_KNOWLEDGE: KnowledgeCategory[] = [TACTICS, ENDGAMES, OPENINGS, OPENINGS_DEEP, MIDDLEGAME, POSITIONAL, TIME_MGMT, MEMORY, ROADMAP, PSYCHOLOGY];

export function findEntryById(id: string): KnowledgeEntry | undefined {
  for (const cat of COACH_KNOWLEDGE) {
    const e = cat.entries.find(x => x.id === id);
    if (e) return e;
  }
  return undefined;
}

export function entriesByDifficulty(d: Difficulty): KnowledgeEntry[] {
  return COACH_KNOWLEDGE.flatMap(cat => cat.entries).filter(e => e.difficulty === d);
}

export type Phase = "opening" | "middlegame" | "endgame";

export type PhaseDetail = {
  phase: Phase;
  label: string;
  reason: string;     // почему именно эта стадия (1 строка)
  totalPieces: number;
  hasQueens: boolean;
};

export function detectPhase(fen: string, moveCount: number): PhaseDetail {
  const board = fen.split(" ")[0];
  const pieces = board.replace(/[1-8/]/g, "");
  const totalPieces = pieces.length;
  const hasQueens = /[Qq]/.test(pieces);
  const minorPieces = (pieces.match(/[NBnb]/g) || []).length;
  const majorPieces = (pieces.match(/[QRqr]/g) || []).length;

  // Эндшпиль — мало материала или нет ферзей при сильно ограниченных фигурах
  if (totalPieces <= 12) {
    return { phase: "endgame", label: "Эндшпиль", reason: `только ${totalPieces} фигур на доске`, totalPieces, hasQueens };
  }
  if (!hasQueens && (minorPieces + majorPieces) <= 6) {
    return { phase: "endgame", label: "Эндшпиль", reason: "ферзей нет, фигур мало", totalPieces, hasQueens };
  }

  // Дебют — мало ходов и почти все фигуры на доске
  if (moveCount < 14 && totalPieces >= 26) {
    return { phase: "opening", label: "Дебют", reason: `ход ${Math.ceil(moveCount / 2) || 1}, фигуры ещё развиваются`, totalPieces, hasQueens };
  }
  if (moveCount < 8) {
    return { phase: "opening", label: "Дебют", reason: "первые ходы", totalPieces, hasQueens };
  }

  return { phase: "middlegame", label: "Миттельшпиль", reason: `${totalPieces} фигур, идёт борьба за позицию`, totalPieces, hasQueens };
}


export function usePricingT() {
  const { lang } = useI18n();
  return (key: string, vars?: Record<string, string | number>): string => {
    const raw = dict[lang][key] ?? dict.en[key] ?? key;
    if (!vars) return raw;
    return Object.keys(vars).reduce(
      (acc, k) => acc.replace(`{${k}}`, String(vars[k])),
      raw,
    );
  };
}

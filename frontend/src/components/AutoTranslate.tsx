// Sort keys by length descending so longer phrases match first
const sortedKeys = Object.keys(dict).sort((a, b) => b.length - a.length);

function translateText(text: string): string {
  let result = text;
  for (const key of sortedKeys) {
    if (result.includes(key)) {
      result = result.split(key).join(dict[key]);
    }
  }
  return result;
}

function walkAndTranslate(node: Node) {
  if (node.nodeType === Node.TEXT_NODE) {
    const original = node.textContent || "";
    const trimmed = original.trim();
    if (!trimmed) return;
    const translated = translateText(original);
    if (translated !== original) {
      node.textContent = translated;
    }
    return;
  }

  // Skip script, style, textarea, input, code, pre
  if (node.nodeType === Node.ELEMENT_NODE) {
    const tag = (node as Element).tagName;
    if (["SCRIPT", "STYLE", "TEXTAREA", "INPUT", "CODE", "PRE", "SVG"].includes(tag)) return;

    // Translate placeholder and title attributes
    const el = node as HTMLElement;
    if (el.getAttribute("placeholder")) {
      const orig = el.getAttribute("placeholder")!;
      const tr = translateText(orig);
      if (tr !== orig) el.setAttribute("placeholder", tr);
    }
    if (el.getAttribute("title")) {
      const orig = el.getAttribute("title")!;
      const tr = translateText(orig);
      if (tr !== orig) el.setAttribute("title", tr);
    }
  }

  const children = node.childNodes;
  for (let i = 0; i < children.length; i++) {
    walkAndTranslate(children[i]);
  }
}

export function AutoTranslate({ children }: { children: React.ReactNode }) {
  const { lang } = useI18n();
  const containerRef = useRef<HTMLDivElement>(null);
  const observerRef = useRef<MutationObserver | null>(null);

  useEffect(() => {
    if (!containerRef.current) return;

    if (lang === "ru") {
      // Initial translation
      walkAndTranslate(containerRef.current);

      // Observe DOM changes (React re-renders, dynamic content)
      observerRef.current = new MutationObserver((mutations) => {
        for (const mutation of mutations) {
          if (mutation.type === "childList") {
            mutation.addedNodes.forEach((node) => walkAndTranslate(node));
          } else if (mutation.type === "characterData") {
            walkAndTranslate(mutation.target);
          }
        }
      });

      observerRef.current.observe(containerRef.current, {
        childList: true,
        subtree: true,
        characterData: true,
      });
    }

    return () => {
      observerRef.current?.disconnect();
      observerRef.current = null;
    };
  }, [lang]);

  // When switching back to EN, force a re-render to restore original text
  // by changing the key
  return (
    <div ref={containerRef} key={lang} style={{ display: "contents" }}>
      {children}
    </div>
  );
}
const PROGRESS_KEY = "aevion-smeta-practice-progress-v1";

export interface PracticeAttempt {
  exerciseId: string;
  picked: number;
  correct: boolean;
  attempts: number;
  ts: number;
}

export function loadPracticeProgress(): Record<string, PracticeAttempt> {
  if (typeof window === "undefined") return {};
  try {
    const raw = localStorage.getItem(PROGRESS_KEY);
    return raw ? (JSON.parse(raw) as Record<string, PracticeAttempt>) : {};
  } catch {
    return {};
  }
}

export function savePracticeAttempt(a: PracticeAttempt): void {
  if (typeof window === "undefined") return;
  try {
    const all = loadPracticeProgress();
    const prev = all[a.exerciseId];
    all[a.exerciseId] = {
      ...a,
      attempts: (prev?.attempts ?? 0) + 1,
      // Раз сдал правильно — фиксируем correct: true (даже если потом промахнётся)
      correct: prev?.correct || a.correct,
    };
    localStorage.setItem(PROGRESS_KEY, JSON.stringify(all));
    window.dispatchEvent(new CustomEvent("aevion-smeta-progress-update"));
  } catch {}
}

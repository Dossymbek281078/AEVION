/**
 * Учебный контент курса «Сметное дело в РК».
 * Каждый уровень состоит из последовательных уроков. Урок = теория (markdown)
 * + опциональные интерактивные мини-тесты (мгновенная проверка).
 *
 * Прогресс по урокам хранится в localStorage отдельно от прогресса уровня
 * (useProgress) — урок «прочитан» когда студент дошёл до конца + ответил на
 * все тесты (если они есть).
 */

export interface QuizMC {
  kind: "mc"; // multiple-choice (один правильный)
  question: string;
  options: string[];
  correct: number; // index в options
  explanation: string;
}

export interface QuizFill {
  kind: "fill"; // вписать число / короткий ответ
  question: string;
  /** Принимаются ответы из этого массива (case-insensitive, trim). Числа сравнивать
   *  с допуском ±1% (если строка парсится как число). */
  accepts: string[];
  hint?: string;
  explanation: string;
}

export type Quiz = QuizMC | QuizFill;

export interface Lesson {
  id: string;
  level: number;
  title: string;
  /** Длительность в минутах (для оценки и прогресс-бара). */
  durationMin: number;
  /** Markdown-контент. Поддерживает синтаксис нашего <Markdown />. */
  content: string;
  /** Интерактивные тесты в конце урока (опционально). */
  quizzes?: Quiz[];
}

